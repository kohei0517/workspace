
public class FourArithmeticOperations {
	public static void main(String[] args) {
		int num1 = 6;
		int num2 = 3;
		
		int ans1 = num1 + num2;
		int ans2 = num1 - num2;
		int ans3 = num1 * num2;
		int ans4 = num1 / num2;
		
		System.out.println(ans1);
		System.out.println(ans2);
		System.out.println(ans3);
		System.out.println(ans4);
	}
}
